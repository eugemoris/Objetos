    private void SeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SeleccionarActionPerformed
       //Se selecciona el archivo a utilizar
        JFileChooser chooser = new JFileChooser(ultimaRuta); // PERMITO LA SELECCION DE UN UNICO ARCHIVO
        
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY); // solo seleccionar achivos
        chooser.setMultiSelectionEnabled(false);//para que no se puedan seleccionar multiples archivos

        FileFilter filtro = new FileNameExtensionFilter("Tipo de archivo", "xls"); //FILTRO PARA QUE APAREZCAN SOLO ARCHIVOS CON EXTENSION XLS
        chooser.setFileFilter(filtro);

        int seleccion = chooser.showOpenDialog(null); //AGREGO RUTA DE ARCHIVO
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            ruta.setText(chooser.getSelectedFile().getAbsolutePath());
            rutaExcel = chooser.getSelectedFile().getAbsolutePath();
            ultimaRuta = rutaExcel;
        }
        seleccionEncuesta = true;
        if ((seleccionEncuesta) && (seleccionTipoEncuesta)) {
            habilitar();
        }
    }//GEN-LAST:event_SeleccionarActionPerformed

