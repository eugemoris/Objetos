    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try{
        //SE obtiene el JSONObject del test pedido, se procesa y se almacena en la carpeta de los perfiles
        AdministradorPerfil ap = new AdministradorPerfil();
        List<JSONObject> objetos = lector.readExcelFile(new File(rutaExcel));
        JSONObject resultado = new JSONObject();
        for(JSONObject jo:objetos){
		resultado = procesador.getResultado(jo);
		if (resultado != null){
			String nombre = (String) jo.get("Nombre");
			if (jo.get("Apellido") != null)
				nombre+=" "+jo.get("Apellido");
                    try {
                        ap.agregarEncuesta(nombre,resultado,procesador.getNombre() );
                    } catch (IOException ex) {
                        Logger.getLogger(VistaProcesadorEncuesta.class.getName()).log(Level.SEVERE, null, ex);
                    }
		}
        }
         JOptionPane.showMessageDialog(null, "El test fue procesado con Exito", "Mensaje", JOptionPane.UNDEFINED_CONDITION);
    }
    catch(Exception exc){
        exc.printStackTrace();
         JOptionPane.showMessageDialog(null, "ERROR" , "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

