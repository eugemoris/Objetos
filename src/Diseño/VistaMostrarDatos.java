    private void verDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verDatosActionPerformed
    try{
        String datos="";
        String nombre="";
        ArrayList<String> grupos = new ArrayList();
        JSONObject encuesta = new JSONObject();
        JSONObject integrantes = new JSONObject();
        JSONObject conformidad = new JSONObject();
        
        if (jList1.isSelectionEmpty())
        {
            JOptionPane.showMessageDialog(null, "Debe seleccionarse un grupo", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        nombre= (String) defaultLista1.getElementAt(jList1.getSelectedIndex());
        encuesta= admPerfil.getEncuestaGrupal(nombre, "feedBackSymlog");
        integrantes= admPerfil.getEncuestaGrupal(nombre, "integrantes");
        conformidad = admPerfil.getEncuestaGrupal(nombre, "conformidad");
        
        datos = datos + "Grupo " + nombre +": " +"\n";
            
        //Se acomodan los Integrantes
        datos = datos + "Integrantes: "  + "\n";
        datos = datos +  integrantes.get("integrantes");
        datos += "\n";
            
        //Se acomodan datos Symlog
        datos = datos + "Symlog Grupal: " + "\n";
        Set<String> setSymlog = encuesta.keySet();
        for (String s: setSymlog)
        {
            if((!s.startsWith("lista"))&&(!s.startsWith("Test")))
                datos = datos + s +  ": " + encuesta.get(s) + " - ";
        }
        datos += "\n";
            
        //Se acomodan las preguntas de conformidad con sus promedios
        Set<String> set = conformidad.keySet();
        for (String s : set)
        {
            if(!s.startsWith("lista"))
            {
                datos = datos + s +": "+  conformidad.get(s) + "\n";
            }
        }
        datos= datos + "\n";
        JOptionPane.showMessageDialog(null, datos);
    }
    catch(Exception exc){
        exc.printStackTrace();
         JOptionPane.showMessageDialog(null, "error en el sistema: no se pueden mostrar datos" , "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_verDatosActionPerformed

