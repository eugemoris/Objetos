    private void cargarLista()
    {
        File fileFolder = new File("./PerfilesGrupales");
        File[] ficheros = fileFolder.listFiles();
        
        if (ficheros.length == 0){
            JOptionPane.showMessageDialog(null, "No se posee grupos por el momento", "Error", JOptionPane.ERROR_MESSAGE);
            Main obj = new Main();
            obj.setVisible(true); //SE NOS HACE VISIBLE LA PANTALLA
            dispose();
        }
        for (int x = 0; x < ficheros.length; x++) {
            defaultLista1.addElement(ficheros[x].getName());
        }
        jList1.setModel(defaultLista1);
    }

